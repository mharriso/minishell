#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list *new;
	t_list *res;

	if (!lst && f && del)
		return (NULL);
	res = NULL;
	while (lst && f)
	{
		if (!(new = ft_lstnew(f(lst->data))))
		{
			ft_lstclear(&res, del);
			return (NULL);
		}
		ft_lstadd_back(&res, new);
		lst = lst->next;
	}
	return (res);
}
