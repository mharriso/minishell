#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t count)
{
	size_t		i;

	i = 0;
	while (i < count)
	{
		((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
		if (((unsigned char *)src)[i] == (unsigned char)c)
			return (dst + i + 1);
		i++;
	}
	return (NULL);
}
